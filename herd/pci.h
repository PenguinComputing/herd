#ifndef PCI_H
#define PCI_H

#include "mce.h"
#include "cpu.h"

// Code responsible to scan the PCI related informations from AMD
// nodes present in the system.

void pci_init();
void pci_exit();

// Read a 32-bit value from PCI configuration space.
u32 pci_read_config(int bus, int device, int function, int offset);

int pci_num_nodes();

// Return the Opterion family number (0x0f for dual cores, 0x1f for
// quad cores).
int pci_opteron_family();

// Return the revision of the Opteron core as a single letter, as 'F'
// or 'D'.
char pci_opteron_revision();

// Return data structre with Cpu information and methods.
Cpu* pci_get_cpu();


#endif
